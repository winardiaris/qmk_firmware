void render_status_layer(void) {
    bool blink = (timer_read() % 1000) < 500;

    if (!logo) {
        // first line
        oled_write_ln_P(PSTR("_EGO_PLNCK-----"), false);
        // second line
        if (layer_state_is(_FN1)) {
            oled_write_ln_P(blink ? PSTR("_FN1_") : PSTR(" FN1 "), false);
        } else if (layer_state_is(_FN2)) {
            oled_write_ln_P(blink ? PSTR("_FN2_") : PSTR(" FN2 "), false);
        } else if (layer_state_is(_FN3)) {
            oled_write_ln_P(blink ? PSTR("_FN3_") : PSTR(" FN3 "), false);
        } else if (layer_state_is(_QWERTY)) {
            oled_write_ln_P(blink ? PSTR("_QWE_") : PSTR(" QWE "), false);
        } else if (layer_state_is(_COLEMAK)) {
            oled_write_ln_P(blink ? PSTR("_COL_") : PSTR(" COL "), false);
        } else {
            oled_write_ln_P(blink ? PSTR("> _  ") : PSTR(">    "), false);
        }
    }
}

void oled_task_user(void) {

    if (timer_elapsed(oled_timer) > 20000) {
        oled_off();
        return;
    }

    if (scroll) {
        oled_scroll_left();
    } else {
        oled_scroll_off();
    }

    render_egoplanck_logo();
    render_status_layer();
}
